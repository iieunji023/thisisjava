
package ch02.sec01;

public class IntegerLiteralExample {
	
	public static void main(String[] args) {
		int val1 = 0b1011;
		int val2 = 0206;
		int val3 = 365;
		int val4 = 0xB3;
		
		System.out.println(val1);
		System.out.println(val2);
		System.out.println(val3);
		System.out.println(val4);
		
	}

}
